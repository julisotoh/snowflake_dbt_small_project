 --TASK 
CREATE OR REPLACE TASK TASK_ACTUALIZAR_RESUMEN
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
    WHEN SYSTEM$STREAM_HAS_DATA('STR_FACT_INCIDENTES')
AS
INSERT INTO DDS.INCIDENTES_DIA (FECHA, BARRIO, TIPO_ACCIDENTE, ANIO, MES, DIA, TOTAL_INCIDENTES)
    SELECT 
        FECHA, BARRIO, TIPO_ACCIDENTE, ANIO, MES, DIA,
        COUNT(*) AS TOTAL_INCIDENTES
    FROM STR_FACT_INCIDENTES
    WHERE METADATA$ACTION = 'INSERT'
    GROUP BY FECHA, BARRIO, TIPO_ACCIDENTE, ANIO, MES, DIA;

--activate the task
ALTER TASK TASK_ACTUALIZAR_RESUMEN RESUME;

--validate the status 
SHOW TASKS LIKE 'TASK_ACTUALIZAR_RESUMEN';




