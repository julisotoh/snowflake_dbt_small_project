--Dynamic Table
CREATE DYNAMIC TABLE INCIDENTES_DIA
    TARGET_LAG = '15 minutes'
    WAREHOUSE = COMPUTE_WH
AS
SELECT count(*) AS TOTAL_INCIDENTES, gravedad_accidente,  barrio, tipo_accidente, anio, mes, dia
FROM DDS.FACT_INCIDENTES
GROUP BY  gravedad_accidente, barrio, tipo_accidente, anio, mes, dia
order by 1 desc


SELECT * FROM INCIDENTES_DIA
ORDER BY TOTAL_INCIDENTES DESC
LIMIT 20;

--to see the status of the dynamic table
SHOW DYNAMIC TABLES LIKE 'INCIDENTES_DIA';

